function escapeXml(str: string): string {
  return str
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;");
}

function installAlfredWorkflow(): void {
  const prefsPath = getAlfredPrefsPath();

  if (!prefsPath) {
    console.error(
      "Alfred preferences not found. Is Alfred installed?\n" +
      "Checked macOS defaults and ~/Library/Application Support/Alfred/",
    );
    process.exit(1);
  }

  const workflowsDir = resolve(prefsPath, "workflows");
  const snipBin = getSnipPath();
  const config = loadConfig();
  const maxResults = config.alfred?.maxResults ?? 20;

  // Find existing workflow by bundle ID, or create new UUID-based directory
  let workflowDir = findExistingWorkflow(workflowsDir);
  if (workflowDir) {
    console.log("Updating existing Snip Search workflow...");
  } else {
    const uuid = crypto.randomUUID().toUpperCase();
    workflowDir = resolve(workflowsDir, `user.workflow.${uuid}`);
  }
  mkdirSync(workflowDir, { recursive: true });

  const plist = generateInfoPlist(snipBin, maxResults);
  writeFileSync(resolve(workflowDir, "info.plist"), plist, "utf-8");

  // Copy the workflow icon (non-fatal if it fails)
  try {
    copyFileSync(WORKFLOW_ICON_PATH, resolve(workflowDir, "icon.png"));
  } catch {
    console.warn("Warning: Could not write workflow icon");
  }

  console.log("Installed Alfred workflow: Snip Search");
  console.log();
  console.log(`  Keyword: snip`);
  console.log(`  Location: ${workflowDir}`);
  console.log();
  console.log("Actions:");
  console.log("  Enter        Copy snippet to clipboard");
  console.log("  Cmd+Enter    Copy snippet to clipboard");
  console.log("  Alt+Enter    Open snippet file (default app)");
  console.log("  Ctrl+Enter   Reveal snippet in Finder");
  console.log("  Space        Quick Look preview");
  console.log();
  console.log("Tip: Set max results with: snip config set alfred.maxResults 30");
  console.log("Tip: Override snip binary with SNIP_BIN in Alfred's Workflow Environment Variables");
  console.log("       (Alfred Preferences → Workflows → Snip Search → Configure Workflow → Environment Variables)");
}

export async function installShellCompletions(program: Command, shell?: string): Promise<void> {
  const targetShell = shell || detectShell();

  if (!["bash", "zsh", "fish"].includes(targetShell)) {
    throw new Error(`Unsupported shell: ${targetShell}. Supported: bash, zsh, fish`);
  }

  const commands = introspectProgram(program);

  let script: string;
  switch (targetShell) {
    case "zsh":
      script = generateZshCompletions(commands);
      break;
    case "fish":
      script = generateFishCompletions(commands);
      break;
    default:
      script = generateBashCompletions(commands);
      break;
  }

  const outPath = getCompletionPath(targetShell);
  const outDir = resolve(outPath, "..");
  mkdirSync(outDir, { recursive: true });
  writeFileSync(outPath, script, "utf-8");

  let zshSetupMsg: string | undefined;
  if (targetShell === "zsh") {
    const result = ensureZshrcSetup();
    zshSetupMsg = result.message;
  }

  console.log(`Installed ${targetShell} completions for snip.`);
  console.log();
  console.log(getSourceInstruction(targetShell, outPath, zshSetupMsg));
}

function installObsidian(): void {
  if (!isObsidianInstalled()) {
    console.error(
      "Obsidian not found. Install it from https://obsidian.md",
    );
    process.exit(1);
  }

  const config = loadConfig();
  const libraryPath = getLibraryPath(config);

  if (!existsSync(libraryPath)) {
    console.error("Snippet library not initialized. Run `snip init` first.");
    process.exit(1);
  }

  const hasCli = isObsidianCliAvailable();
  const vaultName = hasCli ? getVaultName(libraryPath) : null;
  let step = 1;

  console.log("Obsidian Setup Guide");
  console.log("====================");
  console.log();
  console.log(`Snippet library: ${libraryPath}`);
  console.log();

  if (vaultName) {
    console.log(`Opening vault "${vaultName}"...`);
    execSync(`open -a Obsidian "${libraryPath}"`);
    console.log();
  } else {
    console.log(`Step ${step++}: Open your snippet library as a vault`);
    console.log("  1. Open Obsidian");
    console.log("  2. Click \"Open another vault\" (vault icon in bottom-left)");
    console.log("  3. Click \"Open folder as vault\"");
    console.log(`  4. Select: ${libraryPath}`);
    console.log();
  }

  if (!hasCli) {
    console.log(`Step ${step++}: Enable the Obsidian CLI`);
    console.log("  Settings → General → Command line interface → Enable");
    console.log("  Then follow on-screen instructions to add it to your PATH.");
    console.log();
  }

  console.log(`Step ${step++}: Recommended settings`);
  console.log('  - Settings → Files & Links → Use [[Wikilinks]] → Enable');
  console.log('  - Settings → Files & Links → Default location for new notes → "In the folder specified below"');
  console.log('  - Set the folder to your default snippet type (e.g. "snippets")');
  console.log();
  console.log("Recommended community plugins:");
  console.log("  - Dataview — query snippets by frontmatter fields");
  console.log("  - Tag Wrangler — manage and rename tags");
  console.log("  - Templater — create snippet templates");

  if (hasCli) {
    const vaultFlag = vaultName ? ` vault="${vaultName}"` : "";
    console.log();
    console.log("Obsidian CLI commands for your snippets:");
    console.log(`  obsidian search query=<term>${vaultFlag}`);
    console.log(`  obsidian tags counts${vaultFlag}`);
    console.log(`  obsidian backlinks file=<name>${vaultFlag}`);
    console.log(`  obsidian orphans${vaultFlag}`);
    console.log(`  obsidian unresolved${vaultFlag}`);
  }
}

export function createInstallCommand(program: Command): Command {
  return new Command("install")
    .description("Install integrations and extensions")
    .argument(
      "<integration>",
      "Integration to install (completions, alfred, obsidian)",
    )
    .argument("[shell]", "Shell type for completions (bash, zsh, fish)")
    .action(async (integration: string, shell?: string) => {
      switch (integration) {
        case "completions": {
          try {
            await installShellCompletions(program, shell);
          } catch (err) {
            console.error(err instanceof Error ? err.message : String(err));
            process.exit(1);
          }
          break;
        }

        case "alfred":
          installAlfredWorkflow();
          break;

        case "obsidian":
          installObsidian();
          break;

        default:
          console.error(
            `Unknown integration: ${integration}. Available: completions, alfred, obsidian`,
          );
          process.exit(1);
      }
    });
}
